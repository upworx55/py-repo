

# Type conversion 
# Type conversion is the process of converting the value of one data type to another data type.
# Type conversion is automatically done by Python when necessary.
a = 2 #integer
b = 3.25 #float
sum = a + b #python automatically converts the integer to a float for the addition operation. 


# Type Casting
# Type casting is the conversion of one data type into another by the user explicitly.
c = 4 #integer
d = "5" #string
e = int(d) # Convert string to integer
sum1 = c + e # Now we can add integers

f = float("3") # Convert string to float
g = str(4) # Convert integer to string
h = int(3.5) # Convert float to integer (truncates the decimal part)
i = int("10") # Convert string to integer

