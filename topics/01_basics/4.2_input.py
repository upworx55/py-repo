
# This program calculates the sum of two numbers entered by the user.

first=input("Enter first No: ") # data type of first is string
second=input("Enter second No: ") # data type of second is string

# Calculate the sum
sum=int(first)+int(second) # data type of sum is integer

# Display the sum
print("sum is: " + str(sum)) # data type of sum is converted to string for concatenation with the message.
#The print argument is a string that concatenates the message "sum is: " with the string representation of the sum variable.