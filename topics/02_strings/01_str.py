
# Strings in Python can be defined using single quotes, double quotes, or triple quotes. 
# Triple quotes are particularly useful for multi-line strings.

str1 = "This is a string." # Commonly used string with double quotes
str2 = 'This is also a string.' # Single quotes can be used for strings
str3 = """This is a string that spans multiple lines.""" # Triple double quotes for multi-line strings
str4 = '''This is also a string that spans multiple lines.''' # Triple single quotes for multi-line strings
str5 = "This is Jitenrs's tutorial." # Single quotes can be used inside double quotes
str6 = 'Swami Vivekananda said, "Arise, awake, and stop not till the goal is reached."' # Double quotes can be used inside single quotes

# Escape sequences can be used to include special characters in strings. Some common escape sequences include:
# \n - Newline
# \t - Tab
# \\ - Backslash
# \' - Single quote
# \" - Double quote

# Escape sequence characters can be used to include special characters in strings. For example:
str7 = 'This is Jitenrs\'s tutorial.' # Escape character can be used to include single quotes inside single-quoted strings
str8 = "This is a string with a newline character.\nThis is the second line." # Newline character can be used to create a new line within a string
str9 = "This is a string with a tab character.\tThis is after the tab." # Tab character can be used to create a tab space within a string
str10 = "This is a string with a backslash character.\\ This is after the backslash." # Backslash character can be used to include a backslash in a string
str11 = "This is a string with a double quote character.\" This is after the double quote." # Double quote character can be used to include a double quote in a string

print(str7)
print(str10)
print(str11)
