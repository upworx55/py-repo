
# Indexing in Python Strings
str = "apna college"
ch = str[0] # accessing the first character of the string
print(ch) # prints the first character of the string
ch1 = str[1] # accessing the second character of the string
print(ch1) # prints the second character of the string
ch2 = str[2] # accessing the third character of the string
print(ch2) # prints the third character of the string

print(str[3]) # accessing the fourth character of the string
