
# Concatenation of two strings using the + operator

str1 = "apna"
str2 = "collenge"
print(str1+str2) # concatenation of two strings using the + operator


str3 = "my"
str4 = "city"
final_str= str3 + str4 # concatenation of two strings using the + operator and storing it in a variable
print(final_str) 

finalstr1 = str3 + " " + str4 # concatenation of two strings with a space in between
print(finalstr1) # prints the concatenated string with a space in between

